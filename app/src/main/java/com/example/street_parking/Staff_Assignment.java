package com.example.street_parking;

public class Staff_Assignment {
    int id,staff_id, street_id, nmb_cars;
    String time_start, time_end;

    public Staff_Assignment() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStaff_id() {
        return staff_id;
    }

    public void setStaff_id(int staff_id) {
        this.staff_id = staff_id;
    }

    public int getStreet_id() {
        return street_id;
    }

    public void setStreet_id(int street_id) {
        this.street_id = street_id;
    }

    public int getNmb_cars() {
        return nmb_cars;
    }

    public void setNmb_cars(int nmb_cars) {
        this.nmb_cars = nmb_cars;
    }

    public String getTime_start() {
        return time_start;
    }

    public void setTime_start(String time_start) {
        this.time_start = time_start;
    }

    public String getTime_end() {
        return time_end;
    }

    public void setTime_end(String time_end) {
        this.time_end = time_end;
    }
}
